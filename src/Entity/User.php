        // $this->plainPassword = null;
    }

    public function getMobile(): ?string
    {
        return $this->mobile;
    }

    public function setMobile(string $mobile): static
    {
        $this->mobile = $mobile;

        return $this;
    }

    public function getCreated(): ?DateTime
    {
        return $this->created;
    }

    public function getUpdate(): ?DateTime
    {
        return $this->updated;
    }
}
